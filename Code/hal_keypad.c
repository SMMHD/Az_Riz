char keypad_read(void) {
    char keymap[4][4] = {
        {'1','2','3','A'},
        {'4','5','6','B'},
        {'7','8','9','C'},
        {'*','0','#','D'}
    };
    unsigned char r, c;
    
    for(r = 0; r < 4; r++) {
        KEYPAD_PORT = ~(1 << r) | 0xF0; // ??? ???? ?? ???
        wait_ms(2); // ???? ???? ????? ?????
        
        for(c = 0; c < 4; c++) {
            if(!(KEYPAD_PIN & (1 << (c + 4)))) { // ??? ????? ??? ??? ???
                return keymap[r][c];
            }
        }
    }
    return 0; // ??? ????? ????? ????
}

char keypad_wait_key(void) {
    char key, last_key = 0;
    while(1) {
        key = keypad_read();
        if(key != 0 && last_key == 0) { // ????? ??? ??????????? (????? ?????)
            wait_ms(20); // Debounce
            last_key = key;
            return key;
        }
        if(key == 0) {
            last_key = 0;
        }
    }
}